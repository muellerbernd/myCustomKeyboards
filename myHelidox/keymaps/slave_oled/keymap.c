bool process_record_user(uint16_t keycode, keyrecord_t* record)
{
    switch (keycode) {
    case QWERTZ:
        if (record->event.pressed) {
            layer_clear();
            set_single_persistent_default_layer(_QWERTZ);
        }
        return false;
        break;
    case DVORAK:
        if (record->event.pressed) {
            set_single_persistent_default_layer(_DVORAK);
        }
        return false;
        break;
    case LOWER:
        if (record->event.pressed) {
            layer_on(_LOWER);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
        } else {
            layer_off(_LOWER);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
        }
        return false;
        break;
    case RAISE:
        if (record->event.pressed) {
            layer_on(_RAISE);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
        } else {
            layer_off(_RAISE);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
        }
        return false;
        break;
    case ADJUST:
        if (record->event.pressed) {
            layer_on(_ADJUST);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
        } else {
            layer_off(_ADJUST);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
        }
        return false;
        break;
    case NAV:
        if (record->event.pressed) {
            layer_on(_NAV);
        } else {
            layer_off(_NAV);
        }
        return false;
        break;
    }
    return true;
}
